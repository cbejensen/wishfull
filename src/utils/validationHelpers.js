export function validateWish(wish) {
  return new Promise((resolve, reject) => {
    if (!validateUrl(wish.url)) {
      reject(Error('The url is invalid.'))
    }
    if (!validatePrice(wish.price)) {
      reject(Error('The price is invalid.'))
    }
    resolve('Success!')
  })
}

